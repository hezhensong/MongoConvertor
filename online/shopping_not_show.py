from bson import ObjectId
from pymongo import MongoClient

address = '182.92.176.86'
port = 27017


class Shopping:
    def __init__(self):
        pass

    @staticmethod
    def offline_shopping(shopping_id_to_be_offline):
        client = MongoClient(address, port)
        travel1 = client.travel1
        shopping = travel1.shoppings

        shopping.update({'_id': ObjectId(shopping_id_to_be_offline)}, {"$set": {'show_flag': False}})

    @staticmethod
    def online_shopping(shopping_id_to_be_online):
        client = MongoClient(address, port)
        travel1 = client.travel1
        shopping = travel1.shoppings

        shopping.update({'_id': ObjectId(shopping_id_to_be_online)}, {"$set": {'show_flag': True}})


